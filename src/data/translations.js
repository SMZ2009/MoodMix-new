/**
 * 查字典翻译饮品名
 * @param {string} name 英文饮品名
 * @returns {string} 中文名（未找到返回 ''）
 */
export function translateDrinkName(name) {
    if (!name) return '';
    // 精确匹配
    if (DRINK_NAMES[name]) return DRINK_NAMES[name];
    // 不区分大小写匹配
    const key = Object.keys(DRINK_NAMES).find(k => k.toLowerCase() === name.toLowerCase());
    return key ? DRINK_NAMES[key] : '';
}

